package ru.nsu.filippova.model;

/**
 * Текущее состояние партии.
 */
public enum GameStatus {
    /** Игра активна, таймер выполняет шаги. */
    RUNNING,
    /** Игра временно остановлена игроком. */
    PAUSED,
    /** Игрок достиг условия победы. */
    WON,
    /** Игрок столкнулся с препятствием или змейкой. */
    LOST
}
